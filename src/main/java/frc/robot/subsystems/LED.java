package frc.robot.subsystems;

import com.ctre.phoenix6.hardware.CANdle;
import com.ctre.phoenix6.controls.SolidColor;
import com.ctre.phoenix6.signals.RGBWColor;
import edu.wpi.first.wpilibj.DriverStation;
import edu.wpi.first.wpilibj.DriverStation.Alliance;
import edu.wpi.first.wpilibj2.command.SubsystemBase;
import frc.robot.Constants;

/** Subsystem for controlling LEDs via the CTRE CANdle. */
public class LED extends SubsystemBase {

  private final CANdle candle;
  private final int ledStartIndex;
  private final int ledEndIndex;
  private LEDMode mode = LEDMode.OFF;

  public LED() {
    candle =
        new CANdle(
            Constants.LEDConstants.CANDLE_CAN_ID,
            Constants.CanBusConstants.CANIVORE_BUS);
    ledStartIndex = Constants.LEDConstants.STRIP_START_INDEX;
    ledEndIndex =
        Constants.LEDConstants.STRIP_START_INDEX + Constants.LEDConstants.STRIP_LENGTH - 1;
  }

  @Override
  public void periodic() {
    switch (mode) {
      case OFF:
        setSolid(0, 0, 0);
        break;
      case SOLID_GREEN:
        setSolid(0, 255, 0);
        break;
      case SOLID_BLUE:
        setSolid(0, 0, 255);
        break;
      case SOLID_RED:
        setSolid(255, 0, 0);
        break;
      case ALLIANCE:
        if (DriverStation.getAlliance().isPresent()
            && DriverStation.getAlliance().get() == Alliance.Red) {
          setSolid(255, 0, 0);
        } else {
          setSolid(0, 0, 255);
        }
        break;
      default:
        setSolid(0, 0, 0);
    }
  }

  private void setSolid(int r, int g, int b) {
    candle.setControl(
        new SolidColor(ledStartIndex, ledEndIndex)
            .withColor(new RGBWColor(r, g, b, 0)));
  }

  public void setMode(LEDMode mode) {
    this.mode = mode;
  }

  public LEDMode getMode() {
    return mode;
  }

  public enum LEDMode {
    OFF,
    SOLID_GREEN,
    SOLID_BLUE,
    SOLID_RED,
    ALLIANCE
  }
}
